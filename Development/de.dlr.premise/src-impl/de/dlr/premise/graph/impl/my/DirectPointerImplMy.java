package de.dlr.premise.graph.impl.my;

import de.dlr.premise.graph.impl.DirectPointerImpl;

public class DirectPointerImplMy<T> extends DirectPointerImpl<T> {

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((target == null) ? 0 : target.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        DirectPointerImplMy<?> other = (DirectPointerImplMy<?>) obj;
        if (eContainer != null && other.eContainer != null && !eContainer.equals(other.eContainer)) {
            return false;
        }
        if (target == null) {
            if (other.target != null) {
                return false;
            }
        } else if (!target.equals(other.target)) {
            return false;
        }
        return true;
    }
}
